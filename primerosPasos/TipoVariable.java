package ejemplo;

public class TipoVariable {
	public static void main(String[]args) {
		double salario = 1250.56;
		System.out.println(salario);
		
		//Division con variable Double;
		double division = salario / 2;
		System.out.println("Variable tipo double; " + division);
		
		//Division con variables Int.
		int div = 1250 / 3;
		System.out.println("Variable tipo Int: " + div);
	}
}
